    private void BtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCalcularActionPerformed

        NumberFormat Format = NumberFormat.getCurrencyInstance(Locale.getDefault());
        int a =0;
        int dias=0;
        
        
        if(TxtHorasTrabajadas.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane,"Debe digitar las horas Trabajadas"); 
            TxtHorasTrabajadas.requestFocus();
            return;          
        }
        if(Integer.parseInt(TxtHorasTrabajadas.getText()) > 120){
            JOptionPane.showMessageDialog(rootPane,"El Valor de las horas no debe ser mayor a 120 horas"); 
            TxtHorasTrabajadas.requestFocus();
            return;          
        }
        else
        {
            a=(Integer.parseInt(TxtHorasTrabajadas.getText()));
            dias =a/8;
        }
        if(TxtExtrasDiurnas.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane,"Debe digitar las horas  Extras Diurnas"); 
            TxtExtrasDiurnas.requestFocus();
            return;          
        }
        if(TxtExtrasNocturnas.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane,"Debe digitar las horas  Extras Nocturnas"); 
            TxtExtrasNocturnas.requestFocus();
            return;          
        }
        if(Integer.parseInt(TxtExtrasDiurnas.getText()) > 120){
            JOptionPane.showMessageDialog(rootPane,"El Valor de las horas Extras no debe ser mayor a 120 horas"); 
            TxtExtrasDiurnas.requestFocus();
            return;          
        }
        if(TxtPrestamos.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane,"Debe digitar el valor del prestamo"); 
            TxtPrestamos.requestFocus();
            return;          
        }
        if(TxtBonificaciones.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane,"Debe digitar el valor de las bonificaciones"); 
            TxtBonificaciones.requestFocus();
            return;          
        }
        ClsLiquidacionNomina  c = new ClsLiquidacionNomina();
        double salBruto = c.SalarioBasico(dias,Double.parseDouble(TxtSalario.getText()));
        String SalarioB = (String.valueOf(salBruto));
        TxtSalarioBasico.setText(SalarioB);

        double VExtraDiurna = c.ValHorasExtras(Integer.parseInt(TxtExtrasDiurnas.getText()),Double.parseDouble(TxtSalario.getText()));
        String ValorExtraD = (String.valueOf(VExtraDiurna));
        TxtValorExtras.setText(ValorExtraD);
        /*
         
         */
        double VExtraNocturna = c.ValHorasExtrasN(Integer.parseInt(TxtExtrasNocturnas.getText()),Double.parseDouble(TxtSalario.getText()));
        String ValorExtraN = (String.valueOf(VExtraNocturna));
        TxtValorExtrasN.setText(ValorExtraN);
                
        //calculo salario bruto
        double SalarioBruto = c.SalarioBruto( (salBruto),(VExtraDiurna),(VExtraNocturna) );
        String ValorSalarioBruto=(String.valueOf(SalarioBruto));
        TxtSalarioBruto.setText(ValorSalarioBruto);
        
        double Salud = c.CalcAporteSalud(SalarioBruto);
        String ValorSalud = (String.valueOf(Salud));
        TxtSalud.setText(ValorSalud);
        
        double Pension = c.CalcAporteSalud(SalarioBruto);
        String ValorPension = (String.valueOf(Pension));
        TxtPension.setText(ValorPension);
        
        double NetoPagar = c.CalcSalarioNeto( SalarioBruto, Pension, Salud, Double.parseDouble(TxtPrestamos.getText()),  
                Double.parseDouble(TxtAuxilioTransporte.getText()),  Double.parseDouble(TxtBonificaciones.getText()), 
                Double.parseDouble(TxtExtrasDiurnas.getText()), Double.parseDouble(TxtExtrasNocturnas.getText()));
        String ValNetoPagar = (String.valueOf(NetoPagar));
        TxtNetoaPagar.setText(ValNetoPagar);
        
        
        
    }//GEN-LAST:event_BtnCalcularActionPerformed

