    private void BtnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGrabarActionPerformed
        
        
        int numFilas = 0;

                try {
                    liquidacion = new ClsLiquidacionNomina();
                    liquidacion.setCedula((String) CBoxCedPrima.getSelectedItem());
                    liquidacion.setPeriodo((int) CBoxPeriodo.getSelectedItem());
                    liquidacion.setSalhora(Double.parseDouble(TxtSalario.getText()));
                    liquidacion.setAuxTransporte(Double.parseDouble(TxtAuxilioTransporte.getText()));
                    liquidacion.setSalarioBasico(Double.parseDouble(TxtSalarioBasico.getText()));
                    liquidacion.setValorExtrasDiurnas(Double.parseDouble(TxtValorExtras.getText()));
                    liquidacion.setValorExtrasNocturnas(Double.parseDouble(TxtValorExtrasN.getText()));
                    liquidacion.setSalarioBruto(Double.parseDouble(TxtSalarioBruto.getText()));
                    liquidacion.setAporteSalud(Double.parseDouble(TxtSalud.getText()));
                    liquidacion.setAportePension(Double.parseDouble(TxtPension.getText()));
                    liquidacion.setBonificaciones(Double.parseDouble(TxtBonificaciones.getText()));
                    liquidacion.setPrestamos(Double.parseDouble(TxtPrestamos.getText()));
                    liquidacion.setSalarioNeto(Double.parseDouble(TxtNetoaPagar.getText()));
                    
                    
                    
                    numFilas = datos.InsertLiq(liquidacion.getCedula(),
                            liquidacion.getPeriodo(),
                            liquidacion.getSalhora(),
                            liquidacion.getAuxTransporte(),
                            liquidacion.getSalarioBasico(),
                            liquidacion.getValorExtrasDiurnas(),
                            liquidacion.getValorExtrasNocturnas(),
                            liquidacion.getSalarioBruto(),
                            liquidacion.getAporteSalud(),
                            liquidacion.getAportePension(),
                            liquidacion.getBonificaciones(),
                            liquidacion.getPrestamos(),
                            liquidacion.getSalarioNeto()
                            
                            
                            );
                    JOptionPane.showMessageDialog(null, String.valueOf(numFilas) + " Registros Guardados !!!");

                } catch (Exception ee) {
                    System.out.println(ee.getStackTrace());
                    JOptionPane.showMessageDialog(null, String.valueOf(numFilas) + " error !!!");
                }
        
        BtnPrimero.setEnabled(true);
        BtnAnterior.setEnabled(true);
        BtnSiguiiente.setEnabled(true);
        BtnUltimo.setEnabled(true);
        BtnGrabar.setEnabled(false);
        BtnBuscar.setEnabled(true);        
        inhabilitar();
    }//GEN-LAST:event_BtnGrabarActionPerformed

